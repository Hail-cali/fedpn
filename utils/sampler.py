from torch.utils.data import RandomSampler
import torchvision
import torch
from typing import Iterator, Optional, Sequence, List, TypeVar, Generic, Sized
from itertools import combinations

class ClientSampler(RandomSampler):
    data_source: Sized
    replacement: bool

    def __init__(self, data_source: Sized, replacement: bool = False, client_name: Optional[str] = None,
                 num_samples: Optional[int] = None, generator=None) -> None:
        self.data_source = data_source
        self.client = client_name
        self.replacement = replacement
        self._num_samples = num_samples
        self.generator = generator
        self._seed = 42

        if not isinstance(self.replacement, bool):
            raise TypeError("replacement should be a boolean value, but got "
                            "replacement={}".format(self.replacement))

        if not isinstance(self.num_samples, int) or self.num_samples <= 0:
            raise ValueError("num_samples should be a positive integer "
                             "value, but got num_samples={}".format(self.num_samples))

    def make_client(self, data_source):
        if not self.client or self.client == 'server':
            self.data_source = data_source
            return








    @property
    def num_samples(self) -> int:
        # dataset size might change at runtime
        if self._num_samples is None:
            return len(self.data_source)
        return self._num_samples

    def __iter__(self) -> Iterator[int]:
        n = len(self.data_source)
        if self.generator is None:
            generator = torch.Generator()
            generator.manual_seed(self._seed)
            # generator.manual_seed(int(torch.empty((), dtype=torch.int64).random_().item()))
        else:
            generator = self.generator
        if self.replacement:
            for _ in range(self.num_samples // 32):
                yield from torch.randint(high=n, size=(32,), dtype=torch.int64, generator=generator).tolist()
            yield from torch.randint(high=n, size=(self.num_samples % 32,), dtype=torch.int64,
                                     generator=generator).tolist()
        else:
            for _ in range(self.num_samples // n):
                yield from torch.randperm(n, generator=generator).tolist()
            yield from torch.randperm(n, generator=generator).tolist()[:self.num_samples % n]

    def __len__(self) -> int:
        return self.num_samples









